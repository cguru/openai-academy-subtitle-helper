export function normalizeUiLanguage(language) {
  const normalized = String(language ?? "").trim().toLowerCase().replace("_", "-");
  if (!normalized) {
    return DEFAULT_UI_LANGUAGE;
  }

  if (normalized === "zh" || normalized.startsWith("zh-cn") || normalized.startsWith("zh-sg") || normalized.startsWith("zh-hans")) {
    return "zh-Hans";
  }

  for (const code of ["ko", "ja", "es", "fr", "de", "en"]) {
    if (normalized === code || normalized.startsWith(`${code}-`)) {
      return code;
    }
  }

  return DEFAULT_UI_LANGUAGE;
}

export function resolveUiLanguage({ storedLanguage, browserLanguage, navigatorLanguage } = {}) {
  if (storedLanguage && storedLanguage !== "auto") {
    return normalizeUiLanguage(storedLanguage);
  }

  return normalizeUiLanguage(browserLanguage || navigatorLanguage);
}

export function getDefaultTargetLanguage(language) {
  const normalized = normalizeUiLanguage(language);
  return TARGET_LANGUAGE_CODES.has(normalized) ? normalized : "ko";
}

export function getMessage(language, key, params = {}) {
  const normalized = normalizeUiLanguage(language);
  const template = dictionaries[normalized]?.[key] ?? dictionaries[DEFAULT_UI_LANGUAGE][key] ?? key;
  return template.replace(/\{([a-zA-Z0-9_]+)\}/g, (match, name) => {
    const value = params[name];
    return value === undefined || value === null ? match : String(value);
  });
}

export function getTranslator(language) {
  return (key, params) => getMessage(language, key, params);
}
